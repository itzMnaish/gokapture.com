			     <p>
A masterclass for the chocolate connoisseurs, you say? Well, Conosh was here to deliver! Joining hands with Australian celebrity chef Gary Mehigan and Indian pastry chef Pooja Dhingra for a perfectly holiday-themed Masterclass labeled ‘Chocolate Indulgence’ across three Indian metropolitan cities- Delhi, Mumbai and Bengaluru in December 2022. Through Conosh Classified, home-chefs and bakers are provided a golden chance to learn the favourite cooking hacks and how to win hearts with mouthwatering delicacies by globally renowned chefs.
  </p>
<p>
    <a href="https://conosh.com/#">Conosh</a> is a one-of-its-kind initiative that believes in curating intimate culinary experiences that treasure the warmth and joy of sharing a plate of food. It emerged uniting home-chefs online in the throes of the COVID-19 pandemic, and gradually expanded into a budding community of food lovers bonding through Delhi NCR and beyond through their shared enthusiasm for all things gastronomic, primarily in the form of workshops and masterclasses led by leading professionals in the field. Thus, Conosh has helped not only satisfy cravings in a safe, healthy and efficient manner by making doorstep delivery of home-cooked meals possible but also allowed for up-and-coming culinary devotees and entrepreneurs to find the right space that would equip them with support, network and opportunities to enhance their skills. Refining interesting concepts of eating in and eating out, Conosh is creating a strong ripple in the Indian food scene with its food pop-ups.
</p>  

<p>
    Popular chef and restaurateur Gary Mehigan, whom many of us know as one of the original judges on Masterchef Australia, and founder of <a href="https://le15.com/">Le15</a> patisseries and packaged goods with six published books to her name, Pooja Dhingra, toured three major Indian cities in the span of a week to share their secrets from the kitchen and headlaunch the Conosh Classified initiative to introduce to cooking enthusiasts global trends and timeless tricks. Doing its rounds just around Christmas 2022, this Conosh Masterclass enthralled dessert-lovers in Bangalore on December 14, in Mumbai on December 17, and in Delhi on December 21.
</p>


